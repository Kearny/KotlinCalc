package Model

class Math {
    fun adition(a: Double, b: Double): Double {
        return a + b
    }

    fun substraction(a: Double, b: Double): Double {
        return a - b
    }

    fun multiplication(a: Double, b: Double): Double {
        return a * b
    }

    fun division(a: Double, b: Double): Double {
        return a / b
    }
}